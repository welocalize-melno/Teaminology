package com.teaminology.hp.data;

/**
 * Java Class to show TMProfile Terms
 *
 * @author sayeedm
 */
public class TMProfileTerms
{
    private Integer tmProfileInfoId;
    private String sourceTerm;
    private String targetTerm;
    private String sourceLang;
    private String targetLang;
    private String domain;
    private String productLine;
    private String contentType;
    private String company;

    public Integer getTmProfileInfoId() {
        return tmProfileInfoId;
    }

    public void setTmProfileInfoId(Integer tmProfileInfoId) {
        this.tmProfileInfoId = tmProfileInfoId;
    }

    public String getSourceTerm() {
        return sourceTerm;
    }

    public void setSourceTerm(String sourceTerm) {
        this.sourceTerm = sourceTerm;
    }

    public String getTargetTerm() {
        return targetTerm;
    }

    public void setTargetTerm(String targetTerm) {
        this.targetTerm = targetTerm;
    }

    public String getSourceLang() {
        return sourceLang;
    }

    public void setSourceLang(String sourceLang) {
        this.sourceLang = sourceLang;
    }

    public String getTargetLang() {
        return targetLang;
    }

    public void setTargetLang(String targetLang) {
        this.targetLang = targetLang;
    }

    public String getDomain() {
        return domain;
    }

    public void setDomain(String domain) {
        this.domain = domain;
    }

    public String getProductLine() {
        return productLine;
    }

    public void setProductLine(String productLine) {
        this.productLine = productLine;
    }

    public String getContentType() {
        return contentType;
    }

    public void setContentType(String contentType) {
        this.contentType = contentType;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

}
